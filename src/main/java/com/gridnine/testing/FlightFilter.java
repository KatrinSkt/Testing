package com.gridnine.testing;

import java.util.List;

/**
 * Интерфейс для фильтрации списка авиаперелетов.
 * <p>
 * Реализации этого интерфейса должны предоставлять конкретные правила фильтрации
 * для удаления или сохранения перелетов на основе заданных критериев.
 * </p>
 */
public interface FlightFilter {

    /**
     * Фильтрует список авиаперелетов в соответствии с заданными правилами.
     *
     * @param flights список авиаперелетов для фильтрации
     * @return новый список авиаперелетов, прошедших фильтрацию
     */
    List<Flight> filter(List<Flight> flights);
}
